#While

#Ορισμός

'''
Το while είναι ένα εργαλείο με το οποίο μπορείς να επαναλάβεις ένα κομμάτι κώδικα για όσο μια προϋπόθεση ισχύει.
'''

#Σύνταξη

'''
while <προϋπόθεση>:
	<αποτέλεσμα>
'''

#Παράδηγμα

example = 0

while example <= 10:
	example += 1
	print(example)

#Ιδιότιτες while

#1) break

#Ορισμός

'''
Με το break μπορείς να σταματήσεις την while ή την for από το να συνεχίζουν τα ελένχουν το εάν οι επόμενες προϋποθέσεις ισχύουν.
'''

#Παράδηγμα

example = 0
boolian = False

while example < 10:
	if boolian == True:
		break
	example += 1
	print(example)
	boolian = True

#Εδώ θα τυποθεί μόνο το '1' γιατί θέτουμε το boolian = True και το while σταματάει από το break που ενεργοποιήτε μόνο αν το boolian = True'

#2) while True:

#Σύνταξη

