#For

#Σύνταξη

'''
for <στοιχείο> in <ακολουθεία>:
  <αποτέλεσμα>
'''

#Ορισμό
'''
Το for είναι ένα ακόμα εργαλείο, αρκετά παρόμοιο με το while, στο οποίο δείουμε μία ακολουθεία (range, tuple, κτλ.) και αυτό για κάθε ένα στοιχείο το οποίο βρίσκεται σε αυτήν τρέχει μία συγκεκριμένη εντολή.
'''

#Παράδειγμα
my_list = [1,2,3,5,7,9]

for number in my_list:
  print(number)

